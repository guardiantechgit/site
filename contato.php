		(function () {
			var form = document.querySelector('form[action="email-templates/contact-form.php"]');
			if (!form) return;

			// Preenche userAgent e timestamp de início (ms)
			var ua = form.querySelector('input[name="usuario_navegador"]');
			if (ua) { ua.value = navigator.userAgent || 'Não disponível'; }
			var started = form.querySelector('input[name="form_started_at"]');
			if (started) { started.value = Date.now(); }

			// Gera/renova token do reCAPTCHA v3 (tokens expiram rápido)
			var siteKey = '6Lfq-swrAAAAAB-oEqZQ_QKwLGw9xLDJTjTaAhGY';
			function issueToken() {
				if (!window.grecaptcha || !grecaptcha.execute) return;
				grecaptcha.execute(siteKey, {action: 'contact'}).then(function(token) {
					var tgt = form.querySelector('input[name="g-recaptcha-response"]');
					if (tgt) { tgt.value = token; }
				}).catch(function(){ /* silêncio para não impactar UX */ });
			}
			if (window.grecaptcha && grecaptcha.ready) {
				grecaptcha.ready(function () {
					issueToken();
					// renova a cada 90s
					setInterval(issueToken, 90000);
				});
			} else {
				// se ainda não carregou, tenta novamente em 500ms
				var tries = 0, t = setInterval(function(){
					tries++;
					if (window.grecaptcha && grecaptcha.ready) {
						clearInterval(t);
						grecaptcha.ready(function () {
							issueToken();
							setInterval(issueToken, 90000);
						});
					}
					if (tries > 20) { clearInterval(t); }
				}, 500);
			}
		})();
